package lectures;

public class SwitchStatement {

	public static void main(String[] args) {
		
		int num = 1;
		
		switch (num) {
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
		case 3:
			System.out.println("Three");
		default:
			System.out.println("Default");
			
		}
		
		
	}
}
